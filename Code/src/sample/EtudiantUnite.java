package sample;

public class EtudiantUnite {

    int matricule_etu;
    String code_unite;
    int note_cc;
    int note_tp;
    int note_exm;

    public EtudiantUnite(int matricule_etu, String code_unite, int note_cc, int note_tp, int note_exm) {
        this.matricule_etu = matricule_etu;
        this.code_unite = code_unite;
        this.note_cc = note_cc;
        this.note_tp = note_tp;
        this.note_exm = note_exm;
    }
}
